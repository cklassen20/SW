<p> 
	Essa página tem por objetivo instruir o professor sobre a maneira de utilizar a ferramenta SmartWheels, e sobre a utilização e manuseio dos materias 
	disponibilizados para a realização do plano de testes e das atividades propostas. O objetivo da utilização da aplicação SmartWheels é verificar a capacidade de estimular o pensamento computacional
	em crianças de 7 a 12 anos. Os testes e as atividades propostas são algumas das possíveis maneiras de se mensurar a evolução do pensamento computacional 
	de cada aluno. Após a conlusão dos testes e das atividades propostas, o professor poderá avaliar os resultados obtidos e verificar o desempenho atingido
	por cada aluno (ou pelo grupo). Todos os materias necessários estão disponíveis nos links logo mais abaixo nesta página.
</p>
<p>
	A ferramenta SmartWheels tem por objetivo objetivo estimular o pensamento computacional em crianças, utilizando-se de uma aplicação que controla 
	um carrinho criado com a utilização do Arduino. O controlador deste carrinho é um aplicativo para smartphones que possuam o sistema operacional Android.
	Por meio do controlador é possível programar o carro, selecionando uma sequencia de movimentos ao apertar as teclas direcionais no aplicativo. A sequencia
	de movimentos selecionados dá-se o nome de algoritmo. Após a finalização do algoritmo, o mesmo é enviado para o carrinho, que realizará a trajetória 
	programada na aplicação. Ao mesmo tempo, o aplicativo envia os dados da atividade para um servidor na nuvem, que realiza uma análise destes dados, 
	e os trata para que ao final das atividades e dos experimentos, possa-se verificar os resultados obtidos.
</p>	
<p>	
	Em um primeiro momento, o professor deverá repassar o conceito de pensamento computacional às crianças, além de apresentar os 4 pilares do mesmo, sendo
	eles os conceitos de: algoritmos, decomposição, reconhecimento de padrôes e a abstração. Conceitos esses que irão facilitar as crianças a compreender 
	o funcionamento de cada atividade proposta pela ferramenta SmartWheels.
	Os conceitos de algoritmos, decomposição, reconhecimento de padrões e a abstração serão tratados ao durante a utilização do conjunto de atividades 
	propostas pela ferramenta SmartWheels. Conceitos esses que são os 4 pilares do pensamento computacional. 
</p>

